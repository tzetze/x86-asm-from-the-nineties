		int 	16h
		jnz	iskeyesc
		ret
iskeyesc:	xor	ah,ah
		int 	16h
		cmp	al,1bh
		jne	noesc
		jmp	quit2dos
noesc:		ret


putpal:		mov 	al,0
		mov 	dx,03c8h
		out	dx,al

		mov 	cx,256
		mov 	dx,03c9h
loc3:		mov	al,byte ptr 0
		sub	al,cl
		shr	al,1
		out	dx,al
		shr	al,3
		out	dx,al
		shl	al,4
		out	dx,al
		loop	loc3
		ret
