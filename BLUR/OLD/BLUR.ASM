start:		push	@data
		pop	ds
		mov 	ax,0a000h	;screen A000:0000
		mov	es,ax		;ES is a destination pointer
		mov	ax,13h		;set 320x200x256
		int	10h		;graphmode ON!
		call	putpal
		call	convert
		lea	bx,text
                mov	word ptr ds:[mem1],bx

;-MAIN-----------------------------------------------------------------------

neverend:	call	wrt
                mov     cx,7
frame:		push	cx
		call	blur
		call	keys
		pop	cx 
		loop	frame
                jmp     neverend
		


;-SUBROUTINES----------------------------------------------------------------

wrt:		mov	bx,word ptr ds:[mem1]
		mov	al,byte ptr ds:[bx+1]
		xor	ah,ah
		mov	cx,320
		mul	cx
	        add	al,byte ptr ds:[bx]
		adc	ah,0
		mov	di,ax
		xor	ch,ch
		mov	cl,byte ptr ds:[bx+2]
		add 	bx,3
		mov	si,bx
		add	bx,cx
		mov	al,byte ptr ds:[bx]
		cmp	al,0
		jnz	okk
		lea	bx,text
okk:		mov	word ptr ds:[mem1],bx
		
nextchar:	push	cx
		mov	al,byte ptr ds:[si]
		sub	al,32
		mov	bl,18
		mul	bl
		mov	bx,20	
		mul	bx
		call	plotbetu
		add	di,20
		inc	si
		pop	cx
		loop	nextchar
		ret
plotbetu:	push	di
		push	si
		lea	si,chars
		add	si,ax
		mov	cx,20
loc10:		push	cx
		mov	cx,18		
loc11:		mov	al,byte ptr ds:[si]
		cmp     al,0
		jz	none
		mov	byte ptr es:[di],al
none:		inc	si
		inc	di
		loop	loc11
		pop	cx
		add	di,320-18
		loop	loc10

		pop	si
		pop	di
		ret



convert:	lea	si,chars
		mov	cx,len
newpoint:	mov	al,byte ptr ds:[si]
		cmp	al,'.'
		jne	nempont
		mov	al,0
		jmp	lerak
nempont:	mov	al,255
lerak:		mov	byte ptr ds:[si],al
		inc	si				
		loop	newpoint
		ret



blur:	
		push	8000h
		pop	ds
		call	storescr		

		mov	di,321
		mov	si,321

		mov	cx,198
blur1:		push	cx
		mov	cx,318
blur2:		mov	ax,0
		mov	al,byte ptr ds:[si-321]
		add 	al,byte ptr ds:[si-319]
		adc	ah,0
		add 	al,byte ptr ds:[si]
		adc	ah,0
		add 	al,byte ptr ds:[si+321]
		adc	ah,0
		add 	al,byte ptr ds:[si+319]
		adc	ah,0
		xor	dh,dh
		mov 	dl,ah
		mov	ah,al
		xor	al,al
		mov	bx,256*5+37
		div 	bx
		mov	byte ptr es:[di],al
		inc	di
		inc	si
		
		loop	blur2
		add	si,2
		add	di,2
		pop 	cx
		loop	blur1

		call	storescr
		push	@data
		pop	ds
		ret



storescr:	mov	cx,32000		
		mov	si,0			
		mov	di,0
		mov	ax,ds
		mov     bx,es
		mov	es,ax
		mov	ds,bx
		rep	movsw
		mov	ax,ds
		mov     bx,es
		mov	es,ax
		mov	ds,bx
		ret


quit2dos:	mov	ax,03h
		int	10h
		mov	ax,4c00h
		int	21h


